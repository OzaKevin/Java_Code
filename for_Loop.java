/**
 * for_Loop
 */
public class for_Loop {

    public static void main(String[] args) {
    int num=50,i,num2=10; 
    for(i=1;i<=num;i++){
        if (i%2!=0) {
            System.out.println(i);  
        }
     }
     for(i=num2;i!=0;i--){
        if (i%2!=0) {
            System.out.println(i);
        }
     }
    }
}